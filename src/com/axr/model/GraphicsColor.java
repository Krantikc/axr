package com.axr.model;

public class GraphicsColor {

	private int graphicColorId;
	
	private int graphicId;
	
	private String name;
	
	private String image;

	public int getGraphicColorId() {
		return graphicColorId;
	}

	public void setGraphicColorId(int graphicColorId) {
		this.graphicColorId = graphicColorId;
	}

	public int getGraphicId() {
		return graphicId;
	}

	public void setGraphicId(int graphicId) {
		this.graphicId = graphicId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}
	
	
}
