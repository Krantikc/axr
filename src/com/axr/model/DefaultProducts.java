package com.axr.model;

public class DefaultProducts {

	private int productId;
	
	private String name;
	
	private String description;
	
	private int productColorId;
	
	private String productColorName;
	
	private int productGraphicsId;
	
	private String productGraphicsName;
	
	private int productGraphicsColorId;
	
	private String productGraphicsColorName;

	public int getProductId() {
		return productId;
	}
	
	

	public String getName() {
		return name;
	}



	public void setName(String name) {
		this.name = name;
	}



	public String getDescription() {
		return description;
	}



	public void setDescription(String description) {
		this.description = description;
	}



	public void setProductId(int productId) {
		this.productId = productId;
	}

	public int getProductColorId() {
		return productColorId;
	}

	public void setProductColorId(int productColorId) {
		this.productColorId = productColorId;
	}

	public String getProductColorName() {
		return productColorName;
	}

	public void setProductColorName(String productColorName) {
		this.productColorName = productColorName;
	}

	public int getProductGraphicsId() {
		return productGraphicsId;
	}

	public void setProductGraphicsId(int productGraphicsId) {
		this.productGraphicsId = productGraphicsId;
	}


	public String getProductGraphicsName() {
		return productGraphicsName;
	}

	public void setProductGraphicsName(String productGraphicsName) {
		this.productGraphicsName = productGraphicsName;
	}

	public int getProductGraphicsColorId() {
		return productGraphicsColorId;
	}

	public void setProductGraphicsColorId(int productGraphicsColorId) {
		this.productGraphicsColorId = productGraphicsColorId;
	}

	public String getProductGraphicsColorName() {
		return productGraphicsColorName;
	}

	public void setProductGraphicsColorName(String productGraphicsColorName) {
		this.productGraphicsColorName = productGraphicsColorName;
	}
	
	
	
}
